    private void srediFormu() {
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy.");
        jtxtDatumZaposlenja.setText(sdf.format(new Date()));
        jtxtDatumZaposlenja.setEditable(false);
        int broj = 0;
        for (OpstiDomenskiObjekat odo : lista_ranika) {
            Radnik r = (Radnik) odo;
            int a = Integer.parseInt(r.getRadnikID().substring(2, 3));
            if (a > broj) {
                broj = a;
            }
        }
        jtxtRadnikID.setText("R-" + (broj + 1));
                setLocationRelativeTo(null);


    }

