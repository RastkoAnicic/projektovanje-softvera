    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!jErrorIme.getText().trim().equals("") || !jErrorPrezime.getText().trim().equals("")
                || !jErrorUser.getText().trim().equals("") || !jErrorPass.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Niste ispravno popunili sva polja!");
            return;
        }
        Radnik r = new Radnik();
        r.setIme(jtxtIme.getText().trim());
        r.setPrezime(jtxtPrezime.getText().trim());
        r.setUsername(jtxtUser.getText().trim());
        r.setPassword(jtxtIme.getText().trim());
        String ds = jtxtDatumZaposlenja.getText().trim();
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy.");
        try {
            r.setDatumZaposlenja(sdf.parse(ds));
        } catch (ParseException ex) {
            Logger.getLogger(FMRadnik.class.getName()).log(Level.SEVERE, null, ex);
        }
        r.setStatus("ofline");
        r.setRadnikID(jtxtRadnikID.getText().trim());

        try {
            kontrolor.KontrolorAL.getInstance().dodajRadnika(r);
            fmGlavna.azurirajTabelu(r);
                            JOptionPane.showMessageDialog(this, "Sistem je zapamtio radnika", "Unosenje radnika", JOptionPane.INFORMATION_MESSAGE);

            
        } catch (Exception ex) {
                                      JOptionPane.showMessageDialog(this, "Sistem je ne moze da zapamti radnika", "Unosenje radnika", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButton1ActionPerformed


