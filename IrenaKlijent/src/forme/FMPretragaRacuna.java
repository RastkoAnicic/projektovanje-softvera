    public void PostaviDocumentListenerPretraga() {
        DocumentListener documentListener = new DocumentListener() {
            @Override
            public void changedUpdate(DocumentEvent documentEvent) {

                documentListenerPretraga();
            }

            @Override
            public void insertUpdate(DocumentEvent documentEvent) {
                documentListenerPretraga();
            }

            @Override
            public void removeUpdate(DocumentEvent documentEvent) {
                documentListenerPretraga();
            }
        };
        jtxtFilter.getDocument().addDocumentListener(documentListener);
    }

    private void documentListenerPretraga() {
        jbtnPogledajDetalje.setEnabled(false);
        String pretraga = jtxtFilter.getText().trim().toUpperCase();

        List<Racun> filtrirana_lista = new LinkedList<>();
        if (lista_racuna == null) {
            return;
        }
        for (Racun r : lista_racuna) {
            if (r.getDatum().toString().toUpperCase().contains(pretraga)
                    || r.getKlijent().toString().toUpperCase().contains(pretraga)
                    || r.getRadnik().toString().toUpperCase().contains(pretraga)
                     || r.getUkupnaVrednost().toString().toUpperCase().contains(pretraga)) {
                filtrirana_lista.add(r);
            }
        }
        TblModelRacun tbr = new TblModelRacun(filtrirana_lista);
        jtblRacuni.setModel(tbr);
    }

    private Racun popuniRacun() {

        Racun racun = new Racun();
     racun.setKlijent((Klijent) jcbKlijent.getSelectedItem());
        racun.setRadnik((Radnik) jcbRadnik.getSelectedItem());
        racun.setDan((String) jcbDan.getSelectedItem());
        racun.setMesec((String) jcbMesec.getSelectedItem());
        racun.setGodina((String) jcbGodina.getSelectedItem());
        racun.setRasponUkupneVrednosti((String) jcbUkupnaVrednost.getSelectedItem());
        return racun;
       
    }

