    private void jbtnPronadjiRacuneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPronadjiRacuneActionPerformed

        Racun racun = popuniRacun();

        List<Racun> lr = null;
        try {
            lr = Kontrolor.getInstance().vratiRacunePoZadatimKriterijumima(racun);

        } catch (Exception e) {
                  JOptionPane.showMessageDialog(this, "Sistem ne može da nađe račune po zadatim vrednostima", "Pretraga računa", JOptionPane.ERROR_MESSAGE);

            JOptionPane.showMessageDialog(this, e.getMessage());
        }

        TblModelRacun tmr = new TblModelRacun(lr);
        jtblRacuni.setModel(tmr);
        lista_racuna = lr;
        PostaviDocumentListenerPretraga();
    }//GEN-LAST:event_jbtnPronadjiRacuneActionPerformed

